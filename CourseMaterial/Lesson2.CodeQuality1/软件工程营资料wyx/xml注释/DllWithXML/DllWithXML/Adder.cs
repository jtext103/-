namespace DllWithXML
{
    public class Adder
    {
        /// <summary>
        /// 将两个整型数相加，返回和
        /// </summary>
        /// <param name="a">加数a</param>
        /// <param name="b">加数b</param>
        /// <returns>和</returns>
        public int Add(int a, int b)
        {
            return a + b;
        }
    }
}
